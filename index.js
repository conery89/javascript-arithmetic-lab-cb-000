
function add(A, B) {
return A + B
}
function subtract(A,B) {
  return A - B
}
function multiply(A,B) {
  return A * B
}
function divide(A,B) {
  return A / B
}

function inc(n) {
  return ++n
}

function dec(n) {
  return --n
}

function makeInt(n) {
  return parseInt(n, 10)
}
function preserveDecimal(n) {
  return parseFloat(n)
}
